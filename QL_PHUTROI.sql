	MASOCD			VARCHAR(10) NOT NULL,
	BAC				INT NOT NULL,
	MACM			INT NOT NULL,
	CONSTRAINT PK_GIAOVIEN PRIMARY KEY(MAGV)
);

--------------------- BẢNG CHỨC VỤ
CREATE TABLE CHUCVU
(
	MACV			INT NOT NULL IDENTITY(1,1),
	TENCV			NVARCHAR(50),
	CONSTRAINT PK_CHUCVU PRIMARY KEY(MACV)
);

--------------------- BẢNG ĐỊNH MỨC TIẾT DẠY
CREATE TABLE DINHMUCTIETDAY
(
	MACV				INT NOT NULL,
	SODINHMUCTIETDAY	DECIMAL(18,2),
	CONSTRAINT PK_DINHMUCTIETDAY PRIMARY KEY(MACV, SODINHMUCTIETDAY)
);

--------------------- BẢNG CHI TIẾT CHỨC VỤ
CREATE TABLE CHITIETCHUCVU
(
	MACV			INT NOT NULL,
	MAGV			VARCHAR(10) NOT NULL,
	NAMHOC			VARCHAR(15) NOT NULL,
	CONSTRAINT PK_CHITIETCHUCVU PRIMARY KEY(MAGV, MACV, NAMHOC)
);

--------------------- BẢNG CHỨC DANH
CREATE TABLE CHUCDANHNN
(
	MASOCD			VARCHAR(10) NOT NULL,
	HANGCD			NVARCHAR(50),
	CONSTRAINT PK_CHUCDANH PRIMARY KEY(MASOCD)
);

--------------------- BẢNG BẬC LƯƠNG
CREATE TABLE BACLUONG
(	
	MASOCD			VARCHAR(10) NOT NULL,
	BAC				INT NOT NULL,
	HESOLUONG		DECIMAL(18,2),
	MUCLUONGCOSO	DECIMAL(18,0),
