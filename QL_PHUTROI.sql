ALTER TABLE CHITIETCHUCVU
ADD CONSTRAINT FK_CHITIETCHUCVU_GIAOVIEN FOREIGN KEY(MAGV) REFERENCES GIAOVIEN(MAGV),
	CONSTRAINT FK_CHITIETCHUCVU_CHUCVU FOREIGN KEY(MACV) REFERENCES CHUCVU(MACV)

--------------------- BẢNG PHỤ TRỘI
