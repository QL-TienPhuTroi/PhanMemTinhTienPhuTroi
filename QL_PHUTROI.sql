--------------------- BẢNG PHỤ TRỘI
CREATE TABLE PHUTROI
(
	MAGV			VARCHAR(10) NOT NULL,
	TONGTIETDAY		INT,
	TONGTIETDADAY	INT,
	TONGTIETQUYDINH	INT,
