--------------------------------------------------- [ KHỞI TẠO CÁC BẢNG DỮ LIỆU ] ---------------------------------------------------
--------------------- BẢNG GIÁO VIÊN
CREATE TABLE GIAOVIEN
(
	MAGV			VARCHAR(10),
	TRANGTHAI		BIT,
	HOTEN			NVARCHAR(50),
	NGAYSINH		DATE,
	GIOITINH		NVARCHAR(5),
	DIACHI			NVARCHAR(200),
	SODIENTHOAI		VARCHAR(10),
	CCCD			VARCHAR(12),
	EMAIL			VARCHAR(30),
	NGAYVAOTRUONG	DATE,
	NGAYVAODANG		DATE,
	DONVICONGTAC	NVARCHAR(50),
	DANTOC			NVARCHAR(20),
	TONGIAO			NVARCHAR(25),
